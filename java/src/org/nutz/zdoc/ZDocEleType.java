package org.nutz.zdoc;

public enum ZDocEleType {

    /**
     * 新元素
     */
    NEW,
    /**
     * 普通元素
     */
    INLINE,
    /**
     * 用反引号扩起来的元素
     */
    QUOTE,
    /**
     * 图片
     */
    IMG,
    /**
     * 段内换行
     */
    BR,
    /**
     * 标注
     */
    SUP,
    /**
     * 脚注
     */
    SUB

}
